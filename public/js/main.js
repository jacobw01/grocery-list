const deleteBtn = document.querySelectorAll('.del')
const todoItem = document.querySelectorAll('span.not')
const todoComplete = document.querySelectorAll('span.completed')
const todoAdd = document.querySelectorAll('#add')
const todoSubtract = document.querySelectorAll('#sub')

Array.from(deleteBtn).forEach((el)=>{
    el.addEventListener('click', deleteTodo)
})

Array.from(todoItem).forEach((el)=>{
    el.addEventListener('click', markComplete)
})

Array.from(todoComplete).forEach((el)=>{
    el.addEventListener('click', markIncomplete)
})
// add smurf to plus sign
Array.from(todoAdd).forEach((el)=>{
    el.addEventListener('click', addItem)
})
//add smurf to minus sign
Array.from(todoSubtract).forEach((el)=>{
    el.addEventListener('click', subtractItem)
})

async function deleteTodo(){
   console.log(this.parentNode)
    const todoId = this.parentNode.dataset.id
    try{
        const response = await fetch('todos/deleteTodo', {
            method: 'delete',
            headers: {'Content-type': 'application/json'},
            body: JSON.stringify({
                'todoIdFromJSFile': todoId,
            })
        })
        const data = await response.json()
        console.log(data)
        location.reload()
    }catch(err){
        console.log(err)
    }
}

